public class app{
	public static void main(String[] args) {
		int val1=2,val2=8;
		double r;

		//suma
		r=val1+val2;
		System.out.println(r);
		//resta
		r=val1-val2;
		System.out.println(r);
		//multiplicacion
		r=val1*val2;
		System.out.println(r);
		//divicion
		r=val1/val2;
		System.out.println(r);
	}
}